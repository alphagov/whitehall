asset_manager_ids_for_orphaned_assets.each do |asset_manager_id|
  attachment_data = AttachmentData.joins(:assets)
                                  .where(assets: { asset_manager_id: asset_manager_id })
                                  .first

  attachment = attachment_data.last_attachment(include_deleted_attachables: true)

  if attachment.attachable_type != "Edition"
    logger.info("Asset #{asset_manager_id} is attached to a non-edition attachable (#{attachment.attachable_type}), skipping")
    next
  elsif attachment_data.last_publicly_visible_attachment && !attachment_data.last_publicly_visible_attachment.deleted?
    logger.info("Asset #{asset_manager_id} is publicly visible and should not be redirected or deleted, skipping")
    next
  elsif attachment_data.last_publicly_visible_attachment.nil? && Edition.unscoped.find(attachment.attachable_id).state == "unpublished"
    begin
      AssetManager::AssetUpdater.call(asset_manager_id, { "redirect_url" => attachment_data.redirect_url })
    rescue StandardError => e
      logger.info("Asset #{asset_manager_id} could not be updated with redirect URL due to error: #{e.message}, skipping")
    end
  else
    begin
      AssetManager::AssetDeleter.call(asset_manager_id)
    rescue AssetManager::ServiceHelper::AssetNotFound
      logger.info("Asset #{asset_manager_id} has already been deleted from Asset Manager")
    end
  end
end
